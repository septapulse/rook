package rook.daemon.websocket;

public class ZipManagerException extends Exception {

	private static final long serialVersionUID = 1L;

	public ZipManagerException() {
		super();
	}

	public ZipManagerException(String message, Throwable cause) {
		super(message, cause);
	}

	public ZipManagerException(String message) {
		super(message);
	}

	public ZipManagerException(Throwable cause) {
		super(cause);
	}

}
